package NonPrimitiveDataType;

public class SwichStatement {

	public static void main(String[] args) {
   char  a='a';
		
		switch  (a) {
		
		
		
			
		case 'a': 
			    int x = 190;
			    
		        System.out.println(x); 
		        System.out.println("this is another 1"); 
		       break;
		       
		case 'b': System.out.println("this is 2"); break;
		
		
		 default: System.out.println("this is default case"); break;
		
    
		}
	}



		
		

	}


